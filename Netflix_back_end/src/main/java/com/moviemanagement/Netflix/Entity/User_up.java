package com.moviemanagement.Netflix.Entity;

public class User_up {
	int id;
	String email;
	
	User_up(int id,String email){
		this.id = id;
		this.email = email;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
}
